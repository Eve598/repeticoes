package laco_for;

public class Nome_posicao {

    public static void main(String[] args) {
        String[] nomes = {"Docinho", "Lindinha", "Florzinha", "Huguinho", "Luizinho", "Zezinho"};

        for (int i = 0; i < nomes.length; i++) {
            System.out.println("Posição: " + i + " | Nome: " + nomes[i]);
        }
    }
}
